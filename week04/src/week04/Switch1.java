package week04;

public class Switch1 {

	public static void main(String[] args) {
		char grade = 'A';
		
		switch(grade) {
		case 'A':
			System.out.println("A등급");
			break;
		case 'B':
			System.out.println("B등급");
			break;
		case 'C':
			System.out.println("C등급");
			break;
		case 'D':
			System.out.println("D등급");
			break;
		default:
			System.out.println("ㅋㅋ좀 열심히 해라 임마~");
			break;
		}

	}

}
